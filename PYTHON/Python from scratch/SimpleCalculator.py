num1 = float(input("Enter num 1:"))
operator = input("Choose operator (+, -, *, /):")
num2 = float(input("Enter num 2:"))


if (operator == '+'): {
        print(num1 + num2)
}
elif (operator == '-'): {
    print(num1 - num2)
}
elif (operator == '*'): {
    print(num1 * num2)
}
elif (operator == '/'): {
    print(num1 / num2)
}
else :
    print("Invalid input")

    
    