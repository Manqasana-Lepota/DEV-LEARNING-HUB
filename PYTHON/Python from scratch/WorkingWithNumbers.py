#  Basic Math Operations

a = 10
b = 3

print(a + b) #Addition
print(a - b) #Substration
print(a * b) #Multiplication
print(a / b) #Division(Float)
print(a // b) #Floor Division
print(a % b) #Modulus(remainder)
print(a ** b) #Exponentiation

# Assigning Numbers to Variables
age = 25
price = 99.99
print(age)
print(price)

# Converting Between Types
num = 10
num_float = float(num)
print(num_float)

value = "45"
converted = int(45)
print(converted)

# Built-in Math Functions
print(abs(-7))
print(round(3.1416))
print(round(3.1416, 2))
print(max(8, 3, 10, 1))
print(min(8, 3, 10, 1))

